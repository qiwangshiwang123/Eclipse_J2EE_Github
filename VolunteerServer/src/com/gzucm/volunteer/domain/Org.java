package com.gzucm.volunteer.domain;

public class Org {
	private String orgID;// 机构ID
	private String orgName;// 机构名
	private String practiceNumber;// 执业编号
	private String orgScale;// 机构规模
	private String orgAddress;// 机构地址
	private String orgContactName;// 机构联系人姓名
	private String orgTelPhone;// 机构手机/座机
	private String orgEmail;// 机构邮箱
	private String orgIntroduce;// 机构简介
	private String orgType;// 机构类型
	private String registerTime;// 注册时间
	private String recruitNum;// 招募次数

	public String getOrgID() {
		return orgID;
	}

	public void setOrgID(String orgID) {
		this.orgID = orgID;
	}

	public String getOrgName() {
		return orgName;
	}

	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}

	public String getPracticeNumber() {
		return practiceNumber;
	}

	public void setPracticeNumber(String practiceNumber) {
		this.practiceNumber = practiceNumber;
	}

	public String getOrgScale() {
		return orgScale;
	}

	public void setOrgScale(String orgScale) {
		this.orgScale = orgScale;
	}

	public String getOrgAddress() {
		return orgAddress;
	}

	public void setOrgAddress(String orgAddress) {
		this.orgAddress = orgAddress;
	}

	public String getOrgContactName() {
		return orgContactName;
	}

	public void setOrgContactName(String orgContactName) {
		this.orgContactName = orgContactName;
	}

	public String getOrgTelPhone() {
		return orgTelPhone;
	}

	public void setOrgTelPhone(String orgTelPhone) {
		this.orgTelPhone = orgTelPhone;
	}

	public String getOrgEmail() {
		return orgEmail;
	}

	public void setOrgEmail(String orgEmail) {
		this.orgEmail = orgEmail;
	}

	public String getOrgIntroduce() {
		return orgIntroduce;
	}

	public void setOrgIntroduce(String orgIntroduce) {
		this.orgIntroduce = orgIntroduce;
	}

	public String getOrgType() {
		return orgType;
	}

	public void setOrgType(String orgType) {
		this.orgType = orgType;
	}

	public String getRegisterTime() {
		return registerTime;
	}

	public void setRegisterTime(String registerTime) {
		this.registerTime = registerTime;
	}

	public String getRecruitNum() {
		return recruitNum;
	}

	public void setRecruitNum(String recruitNum) {
		this.recruitNum = recruitNum;
	}

}
